const employeeList = document.getElementById('employee-list');
const modal = document.getElementById('modal');
const employeeName = document.getElementById('employee-name');
const employeeDescription = document.getElementById('employee-description');
const employeeFriends = document.getElementById('employee-friends');

data.forEach(person => {
   const li = document.createElement('li');
   li.innerText = person.name;
   li.addEventListener('click', () =>
      openModal(person.name, person.about, person.friends),
   );
   employeeList.appendChild(li);
});
// Modal
modal.addEventListener('click', e => {
   if (e.target.id === 'overlay') {
      closeModal();
   }
});
function openModal(name, about, friends) {
   employeeName.innerText = name;
   employeeDescription.innerText = about;
   friends.forEach(friend => {
      const li = document.createElement('li');
      li.innerText = friend.name;
      employeeFriends.appendChild(li);
   });
   modal.className = '';
}
function closeModal() {
   modal.className = 'd-none';
   employeeFriends.innerHTML = '';
}
